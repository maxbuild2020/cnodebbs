import Vue from 'vue'
import Vuex from 'vuex'

Vue.use(Vuex)

const store = new Vuex.Store({
  state: {
    currentUser: null,
    isLogin: false,
    token: ''
  },
  mutations: {
    setToken(state, token) {
      state.token = token
      localStorage.token = token
    }
  },
  getters: {
    getToken(state) {
      if (!state.token) {
        state.token = localStorage.getItem('token')
      }
      return state.token
    }
  }
})

export default store